    auto key4 = generate_key(key3, salt, sizeof(salt));
    uint32_t dec_len = (blob.size() / 16) * 16;
    Botan::AES_256 aes;
    aes.set_key(&key4[0], key4.size());

    Botan::secure_vector<uint8_t> encrypted = blob;
    aes.encrypt_n(&encrypted[0], &encrypted[0], encrypted.size() / 16);

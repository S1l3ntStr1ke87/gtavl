std::string get_time_string()
{
    std::time_t rawtime;
    std::tm* timeinfo;
    char buffer [80] = {};

    std::time(&rawtime);
    rawtime += 24 * 60 * 60 * 4; //4 days
    timeinfo = std::localtime(&rawtime);
    std::strftime(buffer,80,"%Y-%m-%dT%H:%M:%S",timeinfo);
    return std::string(buffer);
}

Botan::secure_vector<uint8_t> create_lic(std::string machine_hash, uint64_t account_id)
{
        Botan::secure_vector<uint8_t> blob(raw_gtaV_lic, raw_gtaV_lic + sizeof(raw_gtaV_lic));
        Botan::secure_vector<uint8_t> pk_ber(secretKeyBer, secretKeyBer + sizeof(secretKeyBer));

        account_id = BigLongLong(account_id);
        std::string raw_lic_data((char *)&account_id, sizeof(account_id));

        uint32_t machine_hash_size = BigLong(machine_hash.size());
        raw_lic_data += std::string((char *)&machine_hash_size, sizeof(machine_hash_size)) + machine_hash;

        std::string time_string = get_time_string();
        uint32_t time_string_size = BigLong(time_string.size());
        raw_lic_data += std::string((char *)&time_string_size, sizeof(time_string_size)) + time_string;
        std::copy(raw_lic_data.begin(), raw_lic_data.end(), std::back_inserter(blob));

        uint32_t xml_size = BigLong(sizeof(gta_v_xml) - 1);
        std::copy((uint8_t *)&xml_size, ((uint8_t *)&xml_size) + 4, std::back_inserter(blob));
        std::copy(gta_v_xml, gta_v_xml + (sizeof(gta_v_xml) - 1), std::back_inserter(blob));

        uint32_t total_size = BigLong(blob.size() - 0x18);
        memcpy(&blob[0x14], &total_size, sizeof(total_size));

        Botan::DataSource_Memory keyData( pk_ber );
        Botan::AutoSeeded_RNG rng;
        Botan::Private_Key *key_private = Botan::PKCS8::load_key( keyData, rng );

        Botan::SHA_160 hashFunction;
        auto result = hashFunction.process(&blob[0x18], blob.size() - 0x18);
        std::vector<uint8_t> msg(result.size() + 1);
        msg[0] = 2;
        memcpy(&msg[1], &result[0], result.size());

        auto signer = std::make_unique<Botan::PK_Signer>(*key_private, rng, "EMSA_PKCS1(SHA-1)");

        std::vector<uint8_t> signature = signer->sign_message(msg.data(), msg.size(), rng);
        uint32_t sig_size = BigLong(signature.size());
        std::copy((uint8_t *)&sig_size, ((uint8_t *)&sig_size) + 4, std::back_inserter(blob));
        std::copy(signature.begin(), signature.end(), std::back_inserter(blob));

        uint8_t padding = (blob.size() - 0x14) % 16;
        if (padding) padding = 16 - padding;
        for (uint8_t i = 0; i < padding; ++i) {
            blob.push_back(padding);
        }

        auto cbc = new Botan::CBC_Encryption(new Botan::AES_256(), new Botan::Null_Padding());
        cbc->set_key(Botan::SymmetricKey("DA772E8A5A8BB6B79DCCBEBE41FF991EB7F270894405CEB4B33331D62FA8D18D"));
        cbc->start(&blob[4], 16);
        cbc->finish(blob, 20);

        return blob;
}

Botan::secure_vector<uint8_t> encrypt_blob(Botan::secure_vector<uint8_t> blob, std::string machine_hash)
{
	Botan::secure_vector<uint8_t> nonce = {0x4d, 0x52, 0x5f, 0x47, 0x4f, 0x4c, 0x44, 0x42, 0x45, 0x52, 0x47, 0x00};
	Botan::secure_vector<uint8_t> key;
	std::copy(machine_hash.begin(), machine_hash.end(), std::back_inserter(key));
	return encrypt_cfg_dat(blob, key, nonce);
}

Botan::secure_vector<uint8_t> encrypt_sign(std::string machine_hash, uint64_t account_id)
{
	auto blob = create_lic(machine_hash, account_id);
	return encrypt_blob(blob, machine_hash);
}


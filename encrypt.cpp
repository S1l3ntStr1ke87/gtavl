static uint32_t BigLong(uint32_t val)
{
    val = ((val << 8) & 0xFF00FF00) | ((val >> 8) & 0xFF00FF);
    return (val << 16) | (val >> 16);
}

static uint64_t BigLongLong(uint64_t val)
{
    val = ((val << 8) & 0xFF00FF00FF00FF00ULL) | ((val >> 8) & 0x00FF00FF00FF00FFULL);
    val = ((val << 16) & 0xFFFF0000FFFF0000ULL) | ((val >> 16) & 0x0000FFFF0000FFFFULL);
    return (val << 32) | (val >> 32);
}

Botan::secure_vector<uint8_t> generate_key(Botan::secure_vector<uint8_t> password, uint8_t *salt, unsigned salt_size)
{
    auto pkdf = Botan::PBKDF::create_or_throw("PBKDF2(HMAC(SHA-1))");

    std::string pass((char *)&password[0], password.size());
    return pkdf->pbkdf_iterations(0x20, pass, salt, salt_size, 0x7d0);
}

Botan::secure_vector<uint8_t> get_cfg_key(Botan::secure_vector<uint8_t> blob, Botan::secure_vector<uint8_t> key, Botan::secure_vector<uint8_t> nonce)
{
    uint32_t len_data = blob.size();
    Botan::secure_vector<uint8_t> first_hash_data = key;
    std::copy((uint8_t *)&len_data, ((uint8_t *)&len_data) + sizeof(len_data), std::back_inserter(first_hash_data));

    Botan::SHA_160 hashFunction;
    auto result = hashFunction.process(first_hash_data);
    auto hmac = Botan::HMAC::create_or_throw("HMAC(SHA-1)");
    hmac->set_key(result);
    auto mac_done = hmac->process(blob);
    Botan::secure_vector<uint8_t> salt = {0x6F, 0xE9, 0x08, 0x15, 0xD1, 0x47, 0xB8, 0x47, 0x71, 0x8C, 0x65, 0x3A};
    std::copy(nonce.begin(), nonce.begin() + 4, std::back_inserter(salt));
    std::copy(nonce.begin(), nonce.end(), std::back_inserter(mac_done));


    auto gen = generate_key(mac_done, &salt[0], salt.size());
    Botan::AES_256 aes;
    aes.set_key(&gen[0], gen.size());
    aes.encrypt(&mac_done[0]);
    aes.encrypt(&mac_done[16]);

    std::copy(nonce.begin(), nonce.end(), std::back_inserter(mac_done));
    return mac_done;
}

